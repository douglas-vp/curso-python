# Números em Python
print(5,"\n")      # Número inteiro (int)
print(3.14,"\n")   # Número de ponto flutuante (float)
print(-10,"\n")    # Número negativo

# Operações matemáticas
print(2 + 3,"\n")      # Adição
print(10 - 4,"\n")     # Subtração
print(6 * 7,"\n")      # Multiplicação
print(20 / 5,"\n")     # Divisão
print(10 % 3,"\n")     # Módulo (resto da divisão)
print(2 ** 3,"\n")     # Exponenciação (2 elevado a 3)

