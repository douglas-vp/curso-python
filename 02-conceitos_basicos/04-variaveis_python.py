# Variáveis em Python
# Variáveis são usadas para armazenar valores e dados em um programa
# Em Python, não é necessário declarar o tipo da variável, ele é inferido automaticamente
# Exemplo de criação de variáveis

# Variável do tipo inteiro (int)
idade = 25
print(idade)  # Imprime o valor da variável idade   

# Variável do tipo ponto flutuante (float)
altura = 1.75
print(altura)  # Imprime o valor da variável altura

# Variável do tipo string (str)
nome = "Alice"
print(nome)  # Imprime o valor da variável nome 

# Variável do tipo booleano (bool)
estudando = True
print(estudando)  # Imprime o valor da variável estudando

# Variáveis podem ser reatribuídas a novos valores
idade = 30  # Reatribuindo a variável idade
print(idade)  # Imprime o novo valor da variável idade  

